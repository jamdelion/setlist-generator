function arr2obj(arr) {
  let obj = {};
  arr.map(
    (songName) =>
      (obj[songName] = {
        length: 3,
        vocals: true,
        quiet: true,
        banger: true,
        wellknown: true,
        year: 2006,
        dirty: false,
      })
  );
  return obj;
}

